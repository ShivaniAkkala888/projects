package opeartors;

public class LogicalOperator {

	public static void main(String[] args) {
		// logical operator
		 int x,y;
		boolean z; 
	        x=30;
	        y=40;
	        z=(x<y)&&(x>y);
	        System.out.println("&&:"+z);//AND LOGICAL OPERATOR

	        // OR OPERATOR
	        int a,b;
			boolean c;
	        a=30;
	        b=40;
	        c=(a<b)||(a>b);
	        System.out.println("||:"+c);

	}

}
