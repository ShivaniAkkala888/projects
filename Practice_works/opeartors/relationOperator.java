package opeartors;

public class relationOperator {

	public static void main(String[] args) {
		// relational operator
		int i,j;
		i=254;
		j=2456;
		
		System.out.println( i==j);
		System.out.println(i!=j);
		System.out.println(i>j);
		System.out.println(i<j);
		System.out.println(i>=j);
		System.out.println(i<=j);

	}

}
